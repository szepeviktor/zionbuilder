<template>
	<BasePanel
		:panel-name="$translate('global_settings_panel')"
		panel-id="panel-global-settings"
		v-on:close-panel="closePanel('panel-global-settings')"
		class="znpb-general-options-panel-wrapper"
	>
		<div class="znpb-accordions-wrapper znpb-fancy-scrollbar">

			<OptionsForm
				:schema="schema"
				v-model="savedValues"
				:show-changes="false"
			/>

		</div>
	</BasePanel>
</template>
<script>
import { mapGetters, mapActions } from 'vuex'
import BasePanel from './BasePanel.vue'
import { usePanels, useCSSClasses } from '@data'
import { useOptionsSchema } from '@zb/components'

export default {
	name: 'PanelGlobalSettings',
	components: {
		BasePanel
	},
	setup() {
		const { closePanel } = usePanels()
		const { getSchema } = useOptionsSchema()
		const { getClassesByFilter } = useCSSClasses()

		return {
			closePanel,
			getSchema,
			getClassesByFilter
		}
	},
	computed: {
		...mapGetters([
			'getPageSettings'
		]),
		savedValues: {
			get () {
				return this.getPageSettings
			},
			set (newValues) {
				this.updatePageSettings(newValues)
			}
		},
		filteredClasses () {
			if (this.keyword.length === 0) {
				return this.getClasses
			} else {
				return this.getClassesByFilter(this.keyword)
			}
		},
		schema () {
			const cssClasses = {
				'global_css': {
					type: 'accordion_menu',
					title: this.$translate('global_css_classes'),
					child_options: {
						'global_css_classes': {
							type: 'global_css_classes'
						}
					}
				}
			}
			return Object.assign({}, this.getSchema('pageSettingsSchema'), cssClasses)
		}
	},
	methods: {
		...mapActions([
			'updatePageSettings'
		])
	}
}
</script>
